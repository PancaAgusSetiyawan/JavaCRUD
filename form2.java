    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        //if ((txtKd_kary.getText().equals(""))||(txtNm_kary.getText().equals(""))||(txtBagian.getText().equals(""))){

        if ((txtkode.getText().equals(""))|| (txtnama.getText().equals(""))||(txtbagian.getText().equals(""))){
        JOptionPane.showMessageDialog(null,"Data Harus Lengkap");
        }else{
            try{
                koneksi();
                String sql = "Select kd_kary FROM tb_karyawan WHERE kd_kary ='" + txtkode.getText() + "'";
                ResultSet rs = stat.executeQuery(sql);
                if (rs.next()){
                    JOptionPane.showMessageDialog(null, "Kode" + txtkode.getText() + "Sudah ada");
                }else{
                    String sqlSave = "INSERT INTO tb_karyawan (kd_kary, nm_kary, bagian) VALUES ('" + txtkode.getText() + "', '" + txtnama.getText() + "', '" + txtbagian.getText() +"')";
                    stat.executeUpdate(sqlSave);
                    tampil_data();
                    kosong();
                    btnEdit.setEnabled(false);
                     btnSave.setEnabled(true);
                     btnHapus.setEnabled(false);
                     btnClose.setEnabled(true);
                     btnCancel.setEnabled(true);

                JOptionPane.showMessageDialog(null,"Data Tersimpan");
                }                        
            }catch(Exception e){
                JOptionPane.showMessageDialog(null,"Gagal Menyimpan Data");
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int pesan = JOptionPane.showConfirmDialog(null,"Data Akan Dihapus?","Info",JOptionPane.YES_NO_OPTION);
        if (pesan == 0){
            
     
            if ((txtkode.getText().equals(""))|| (txtnama.getText().equals(""))|| (txtbagian.getText().equals(""))){
                JOptionPane.showMessageDialog(null,"Data harus lengkap");
            }else{
                try{
                    String sql = "Delete From tb_karyawan WHERE kd_kary = '" + txtkode.getText() + "'";
                    stat.executeUpdate(sql);
                    tampil_data();
                    kosong();
                    btnEdit.setEnabled(false);
                     btnSave.setEnabled(true);
                     btnHapus.setEnabled(false);
                     btnClose.setEnabled(true);
                     btnCancel.setEnabled(true);

                    JOptionPane.showMessageDialog(null,"Data telah Dihapus");
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null,"Gagal Menghapus data");
                }
        }
        }   
    }//GEN-LAST:event_btnHapusActionPerformed

