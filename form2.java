    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if (btnEdit.getText().equals("Edit")){
            btnEdit.setText("Update");
            btnSave.setEnabled(false);
            btnHapus.setEnabled(false);
            btnClose.setEnabled(false);
            btnCancel.setEnabled(true);          
        }else{
            
            try{
                String sql = "Update tb_karyawan SET nm_kary = '" + txtnama.getText() + "', bagian = '" + txtbagian.getText() + "' WHERE kd_kary = '" + txtkode.getText() +"'";
                stat.executeUpdate(sql);
                kosong();
                tampil_data();
                JOptionPane.showMessageDialog(null,"Update Sukses","Info",JOptionPane.INFORMATION_MESSAGE);
                   btnEdit.setText("Edit");
                    btnEdit.setEnabled(false);
                    btnSave.setEnabled(true);
                    btnHapus.setEnabled(false);
                    btnClose.setEnabled(true);
                    btnCancel.setEnabled(true);

            }catch(Exception e){
                JOptionPane.showConfirmDialog(null,"Update Gagal","Warning",JOptionPane.ERROR_MESSAGE);
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

